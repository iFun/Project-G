/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */
/**
 * @param {ListNode} l1
 * @param {ListNode} l2
 * @return {ListNode}
 */

var addTwoNumbers = function(l1, l2) {

    let addOne = 0;
    let sum = 0;
    let current = new ListNode();
    output = current;

    while(l1 || l2){
        if(l1){
            sum += l1.val;
            l1 = l1.next;
        }
        if (l2) {
            sum += l2.val;
            l2 = l2.next;
        }
        sum += addOne;
        addOne = 0;
        if(sum > 9){
            sum = sum - 10;
            addOne = 1;
        }
        
        newDigit = new ListNode(sum);
        current.next = newDigit;
        current = current.next;
        sum = 0;
    }
    

    // leading 1 at the end
    if (addOne === 1) {
        newDigit = new ListNode(addOne);
        current.next = newDigit;
        current = current.next;
    }
    return output.next;
    
};


